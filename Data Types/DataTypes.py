# Python Data Types with Examples and Notes

# 1. int (Integer)
num = 10  # Whole numbers, no decimal point
print("num:", num, "| type:", type(num))

# 2. float
price = 99.99  # Numbers with decimal point
print("price:", price, "| type:", type(price))

# 3. str (String)
name = "Alice"  # Text data, enclosed in quotes
print("name:", name, "| type:", type(name))

# 4. bool (Boolean)
is_active = True  # Only True or False values
print("is_active:", is_active, "| type:", type(is_active))

# 5. list
fruits = ["apple", "banana", "cherry"]  # Ordered collection, can change, use []
print("fruits:", fruits, "| type:", type(fruits))

# 6. tuple
coordinates = (10, 20)  # Ordered collection, cannot change, use ()
print("coordinates:", coordinates, "| type:", type(coordinates))

# 7. set
unique_numbers = {1, 2, 3}  # Unordered, unique items, use {}
print("unique_numbers:", unique_numbers, "| type:", type(unique_numbers))

# 8. dict (Dictionary)
student = {"name": "Bob", "age": 21}  # Key-value pairs, use {}
print("student:", student, "| type:", type(student))

# 9. NoneType
result = None  # Represents no value
print("result:", result, "| type:", type(result))